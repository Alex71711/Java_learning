class Person {
    private String name;  // –ø–æ–ª–µ –¥–æ—Å—Ç—É–ø–Ω–æ —Ç–æ–ª—å–∫–æ –≤–Ω—É—Ç—Ä–∏ –∫–ª–∞—Å—Å–∞
    private int age;

    // –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä ‚Äî –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç –æ–±—ä–µ–∫—Ç
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // –ì–µ—Ç—Ç–µ—Ä (–º–µ—Ç–æ–¥ –¥–ª—è —á—Ç–µ–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏—è)
    public String getName() {
        return name;
    }

    // –°–µ—Ç—Ç–µ—Ä (–º–µ—Ç–æ–¥ –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏—è)
    public void setAge(int age) {
        if (age > 0) {   // –ø—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç–∏
            this.age = age;
        }
    }

    public int getAge() {
        return age;
    }
}

public class Encapsulation {
    public static void main(String[] args) {
        Person p = new Person("–ò–≤–∞–Ω", 25);

        // –ü–æ–ª—É—á–∞–µ–º –∏–º—è —á–µ—Ä–µ–∑ –≥–µ—Ç—Ç–µ—Ä
        System.out.println("–ò–º—è: " + p.getName());

        // –ú–µ–Ω—è–µ–º –≤–æ–∑—Ä–∞—Å—Ç —á–µ—Ä–µ–∑ —Å–µ—Ç—Ç–µ—Ä
        p.setAge(30);
        System.out.println("–í–æ–∑—Ä–∞—Å—Ç: " + p.getAge());
    }
}
