// Файл: Variables.java
public class Variables {
    public static void main(String[] args) {
        // Объявление целочисленной переменной (int хранит целые числа)
        int age = 25;

        // Объявление переменной с плавающей точкой (double хранит дробные числа)
        double price = 19.99;

        // Объявление логической переменной (boolean может быть true или false)
        boolean isStudent = true;

        // Объявление символьной переменной (char хранит один символ в одинарных кавычках)
        char grade = 'A';

        // Объявление строки (String — это объект, хранит текст)
        String name = "Иван";

        // Вывод значений переменных в консоль
        System.out.println("Возраст: " + age);
        System.out.println("Цена: " + price);
        System.out.println("Студент? " + isStudent);
        System.out.println("Оценка: " + grade);
        System.out.println("Имя: " + name);
    }
}
